#include <stdio.h>
#include <limits.h>

int main() {
    printf("int max: %d\n", INT_MAX);           // 2,147,483,647
    printf("int min: %d\n", INT_MIN);           // -2,147,483,648
    
    printf("unsigned max: %u\n", UINT_MAX);     // 4,294,967,295
    printf("unsigned min: %u\n", 0);            // 0
    
    return 0;
}

